print(' '.join(random_words))

start_time = time.time()

print("\n")

user_input = input()

end_time = time.time()

errors = 0

for i in range(len(random_words)):
    if random_words[i] != user_input.split()[i]:
        errors += 1

time_taken = end_time - start_time
speed = len(user_input.split()) / time_taken * 60

print(f'Количество ошибок: {errors}')
print(f'Время: {time_taken:.2f} секунд')
print(f'Скорость печати: {speed:.2f} слов в минуту')
